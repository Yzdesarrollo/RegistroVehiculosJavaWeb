    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        
        int ID = Integer.parseInt(request.getParameter("txtdiR"));
        String NOM = request.getParameter("txtNomR");
        String IDVH = request.getParameter("txtRv");
        
        Ruta ruta = new Ruta ();
        ruta.setIdR(ID);
        ruta.setNombre(NOM);
        ruta.setIdVh(IDVH);
        
        if(rutaDAO.insertarR(ruta))
        {
            request.setAttribute("mensaje", "Ruta registrada con exito");
        }
        else
        {
            request.setAttribute("mensaje", "Ruta no registrada");
        }
        
        request.getRequestDispatcher("ruta.jsp").forward(request, response);
    }

